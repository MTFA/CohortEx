        B.GENDER = C.SEXO_TP_SEXO                                             
      GROUP BY 
        C.SEXO_DS 

    UNION 

      SELECT 
        'Total' as "GENDER"
      , (SELECT COUNT(DISTINCT (PATIENT_ID)) FROM FILTER_ADM_DISCH_ENROLLMENT ) as "PATIENT_WITH_ENCOUNTER" 
      , 100.00
      FROM 
        DUAL
    )
